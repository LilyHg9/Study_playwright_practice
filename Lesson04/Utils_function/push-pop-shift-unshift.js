const numbers = [1, 2, 3]; 
const names = ["Alice", "Bob", "Charlie"]; 

// thêm phần tử 4 vào mảng numbers
numbers.push(4);
console.log(numbers);

// xóa phần tử cuối của numbers
numbers.pop()
console.log(numbers);

// thêm phần tử vào đầu mảng

numbers.unshift(0);
console.log(numbers)

names.unshift("David");
console.log(names);

// xóa phần tử đầu của mảng

numbers.shift();
console.log(numbers)


